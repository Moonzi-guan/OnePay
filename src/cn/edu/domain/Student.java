package cn.edu.domain;

public class Student {
	private String id;
	private String student_sid;
	private String student_name;
	private String student_sex;
	private String student_major;
	private String student_class;
	private String student_addr;
	private String student_money;
	private String student_status;
	public String getStudent_sid() {
		return student_sid;
	}
	public void setStudent_sid(String student_sid) {
		this.student_sid = student_sid;
	}
	public String getStudent_name() {
		return student_name;
	}
	public void setStudent_name(String student_name) {
		this.student_name = student_name;
	}
	public String getStudent_sex() {
		return student_sex;
	}
	public void setStudent_sex(String student_sex) {
		this.student_sex = student_sex;
	}
	public String getStudent_major() {
		return student_major;
	}
	public void setStudent_major(String student_major) {
		this.student_major = student_major;
	}
	public String getStudent_class() {
		return student_class;
	}
	public void setStudent_class(String student_class) {
		this.student_class = student_class;
	}
	public String getStudent_addr() {
		return student_addr;
	}
	public void setStudent_addr(String student_addr) {
		this.student_addr = student_addr;
	}
	public String getStudent_money() {
		return student_money;
	}
	public void setStudent_money(String student_money) {
		this.student_money = student_money;
	}
	public String getStudent_status() {
		return student_status;
	}
	public void setStudent_status(String student_status) {
		this.student_status = student_status;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}

	
}
